            </li>
            <li>创作者在「创作者联协名录」的信息仅用于「联协体系」内辅助「创作者多方流动与合作机制」运行。</li>
            <li>「创作者多方流动与合作机制」与「创作者联协名录」的最终解释权归「同好会」所有。「同好会」根据「联协体系」运行实际情况，可对本部分内容进行适时调整，调整后的内容应及时向体系内各联协组织通报。</li>
          </ul>

          <h5 id="section-3">● 三、「联协项目」开展明细：</h5>
          <ol class="text-gray-200">
            <li>「联协组织」在确定开展「联协项目」后，基于联协项目性质填写表格并提交给「同好会」。</li>
            <li>「联协项目」性质基于其类型、开展情况、「联协组织」需求分为以下类型：<br>
              a. 联协活动：一方或多方共同举办或参与的线上/线下活动，包括但不限于线上主题活动，线下漫展展会等。<br>
              b. 联协创作：一方或多方通过「创作者多方流动与合作机制」进行创作产出。<br>
              c. 联协公招：一方通过「联协体系」向多方寻求创作者协助，发布「联协项目公招」<br>
              d. 其他类型：除以上情况外，开展的其他项目类型。
            </li>
            <li>在充分保障创作者正常工作量、合法权益及创作意愿的前提下，各「联协组织」的创作者可自由通过「联协体系」参与「联协项目」。</li>
            <li>「联协项目」由「同好会」协助协调多方沟通，并由 发起方与各参与方共同协商制定方案，多方地位平等。</li>
            <li>「联协项目」开展前，须由双方必须明确以下信息：<br>
              a. 参与的「联协组织」<br>
              b. 项目分工与负责人<br>
              c. 进度节点（即未来每一时间点进度应达到多少）<br>
              d. 收益分配比例（含创作者个人收益与组织收益）<br>
              e. 署名方式与宣传责任
            </li>
            <li>「联协项目」开展期间，参与对应「联协项目」与「同好会」应共同定期更新「联协项目」的项目进度。</li>
            <li>项目完成后，须在体系内发布成果总结，如参与者名单、成果链接、收益分配情况（可脱敏）。</li>
            <li>「联协体系」内各组织的合作信息、资源清单、活动安排应在「同好会」官网或内部协作群聊保持更新，确保信息对称与可追溯。</li>
            <li>本部分条款最终解释权归「同好会」所有。「同好会」根据「联协体系」运行实际情况，可对本部分内容进行适时调整，调整后的内容应及时向体系内各联协组织通报。</li>
          </ol>

