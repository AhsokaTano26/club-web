              e. 「同好会」对创作者任何形式针对「同好会」、任何「联协组织」的恶意行为（如贬低，歧视，攻击等）侵犯组织权益的行为，将坚决予以抵制。<br>
              若创作者违反上述原则，「同好会」有权将相关人员移出「联协体系」，并不再对其提供服务。
            </li>
            <li>在「同好会」获得各组织创作者人员相关名单后，各创作者将进入「创作者联协名录」，并启用「创作者多方流动与合作机制」。</li>
            <li>「创作者多方流动与合作机制」是「同好会」通过「创作者联协名录」信息，向各「联协组织」与创作者提供「联协项目」或非「联协项目」的信息，从而实现创作者拓展更多项目机会与创作空间的「联协体系」机制。其详细工作机制如下：<br>
