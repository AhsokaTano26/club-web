          <h5 id="section-2">● 二、创作者多方流动与合作机制：</h5>
          <ul class="text-gray-200">
            <li>创作者是指具备独立创作能力，以原创或二次创作为主要方式，参与文学、视觉艺术、动态影像、音频、互动内容等创意领域的个体。在「同好会」「联协体系」内，创作者包括但不限于以下任一角色的从业者或爱好者：<br>
              a. 文字创作者：小说作者、脚本作家、设定撰写者、文案策划等<br>
              b. 视觉创作者：插画师、原画师、概念设计师、漫画作者、平面设计师等<br>
              c. 影像创作者：动画师、视频剪辑师、特效制作师、分镜师等<br>
              d. 音声创作者：配音演员、音效设计师、音乐制作人、音频编辑等<br>
              e. 交互或综合创作者：游戏策划、互动叙事作者、跨媒介艺术创作者等<br>
              f. 其他类型创作者：在上述分类未提及的创作者。
            </li>
            <li>在各「联协组织」了解本组织内创作者意愿后，向「同好会」提供人员名单。在对于名单上确认后，即获得「同好会」对于创作者的身份认定。</li>
            <li>「同好会」对在「联协体系」内的创作者遵循以下原则：<br>
              a. 无论创作者以创作为主要职业，还是作为爱好参与，只要具备相应创作能力并遵守体系规范，均可被视为体系内的创作者。<br>
              b. 组织归属与独立创作并存，创作者具有归属署名权，即在「联协项目」或相关作品中自由选择是否署名自己属于某「联协组织」或「同好会」<br>
              c. 「联协体系」尊重创作者的选择，支持其在体系内自由流动与参与项目。<br>
