            <li>「同好会」为各「联协组织」提供全面的信息发布支持，包括但不限于：<br>
              a. 编写教程与格式说明，指导「联协组织」规范使用官网功能；<br>
              b. 提供文章上传模板，统一内容呈现格式；<br>
              c. 协助上传内容的后续维护与更新。
            </li>
            <li>在「联协体系」内，官网为各「联协组织」提供高度可自定义的专用页面，便于各组织根据自身需求进行个性化展示。各组织在自定义页面时，应确保内容符合本条例及相关规范要求。</li>
            <li>「联协组织」可通过指定格式上传文案，用于以下内容的发布：<br>
              a. 活动宣发：通过日程表等相关页面，展示各组织活动的举办日期、宣传文案等信息，便于体系内外了解与参与；<br>
              b. 「联协项目」公招：通过相关界面发布项目人员招募信息，创作者可通过预留联系方式直接与对应组织人员沟通，实现人才与资源的有效流动；<br>
              c. 项目进展及推文宣发：官网支持高度可自定义的推文宣发页面，便于组织动态展示项目进展、成果及阶段性总结。
            </li>
            <li>官网内容管理：<br>
              a. 官网明确禁止发布任何形式的违法违规或违背「同好会」相关条例及社区规则的内容，包括但不限于涉及违法犯罪、R18、血腥暴力、反动言论等的文字、图片、链接、视频、文件等。<br>
              b. 一经发现或核实存在上述违规内容，「同好会」有权暂停该组织的官网使用资格及相关权益，暂停期限视情节轻重而定。<br>
              c. 对于情节严重的违规行为，「同好会」保留终止该组织「联协体系」资格的权利，并向体系内通报。
            </li>
            <li>本部分条款最终解释权归「同好会」所有。「同好会」根据「联协体系」运行实际情况，可对本部分内容进行适时调整，调整后的内容应及时向体系内各联协组织通报。</li>
          </ol>

          <h5 id="section-5">● 五、「联协组织」与创作者权益保护：</h5>
          <ol class="text-gray-200">
            <li>「联协项目」涉及的知识产权归属，应遵循创作者个人创作成果归创作者所有的基本原则。各「联协组织」在「同好会」统筹协调下开展项目合作时，应在合作启动前以书面形式明确约定各方权利归属及收益分配方案。</li>
            <li>所有「联协项目」在宣传物料、成品展示中须按规定格式规范署名，明确标注以下内容：<br>
              a. 创作者署名：根据实际创作贡献，逐一标注创作者姓名或创作标识；<br>
              b. 所属组织：标注创作者在项目中归属的组织名称；<br>
              c. 合作标识：统一标注「跨世界同好会联协项目」标识及相关「联协组织」标识。
            </li>
            <li>创作者有权选择在项目中归属「同好会」或其原组织进行署名（除特殊情况外）：<br>
              a. 创作者应在项目启动前书面确认署名归属方式；<br>
              b. 创作者选择归属「同好会」的，其署名将列于「同好会」的「联协项目」创作者名录下；<br>
              c. 创作者选择归属原组织的，其署名将列于原组织成员名录下；<br>
              d. 若创作者选择不归属于任何组织，应尊重创作者意愿，不做明确标注；<br>
              e. 署名选择权一经确认，未经创作者本人同意，任何组织不得擅自变更。<br>
              f. 特殊情况：「联协活动」期间，因线下活动特殊性，所有参与线下活动人员须在参与前，必须标明或明确其所属组织。
            </li>
            <li>「同好会」鼓励在项目收益分配中优先保障创作者个人所得，在此基础上合理分配组织收益：<br>
              a. 创作者个人所得应根据实际创作贡献度及约定比例优先分配；<br>
              b. 创作者个人所得分配完毕后，剩余收益由参与项目的「联协组织」按约定比例分配；<br>
              c. 若同好会只作为协调方（即并非联协项目参与组织之一），不参与项目收益分配，其协调工作基于「联协体系」公共服务属性开展。
            </li>
            <li>「同好会」若作为「联协项目」参与组织之一，在同等条件下，可对合作组织给予收益倾斜，以支持其发展：<br>
              a. 同等条件，指创作者贡献度、项目投入成本、市场收益等关键要素基本相当的情形；<br>
              b. 收益倾斜的具体比例由「同好会」与相关组织协商确定，以书面形式确认；<br>
              c. 收益倾斜旨在支持新兴组织或处于发展关键期的「联协组织」。
            </li>
            <li>「同好会」对盗用、侮辱、歧视、恶意诋毁等侵犯创作者权益的行为坚持零容忍原则：<br>
              a. 盗用行为：未经创作者授权，擅自使用、复制、修改或分发其创作成果；<br>
              b. 侮辱与歧视行为：针对创作者或其作品发表侮辱性、贬低性或歧视性言论；<br>
              c. 恶意诋毁行为：散布不实信息，恶意贬损创作者声誉或作品价值。<br>
              d. 处理措施：<br>
